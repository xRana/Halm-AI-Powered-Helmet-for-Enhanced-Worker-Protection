# STEP 5.1: Unified Remapping
# ==========================================

def convert_voc_to_yolo(xml_dir, out_dir):
    os.makedirs(out_dir, exist_ok=True)
    count = 0
    for xml_file in os.listdir(xml_dir):
        if not xml_file.endswith('.xml'): continue

        tree = ET.parse(os.path.join(xml_dir, xml_file))
        root = tree.getroot()
        size = root.find('size')
        w = int(size.find('width').text)
        h = int(size.find('height').text)

        yolo_lines = []
        for obj in root.iter('object'):
            xmlbox = obj.find('bndbox')
            xmin, xmax = float(xmlbox.find('xmin').text), float(xmlbox.find('xmax').text)
            ymin, ymax = float(xmlbox.find('ymin').text), float(xmlbox.find('ymax').text)

            x_center = (xmin + xmax) / 2.0 / w
            y_center = (ymin + ymax) / 2.0 / h
            width = (xmax - xmin) / w
            height = (ymax - ymin) / h
            yolo_lines.append(f"1 {x_center} {y_center} {width} {height}")

        with open(os.path.join(out_dir, xml_file.replace('.xml', '.txt')), 'w') as f:
            f.write("\n".join(yolo_lines))
        count += 1
    print(f"✅ Converted {count} XML files to YOLO format in {out_dir}")

