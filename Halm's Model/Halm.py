def get_eda_dataframe():
    lbl_dir = '/content/halm_final/labels/train'
    data = []
    for lbl in os.listdir(lbl_dir):
        with open(os.path.join(lbl_dir, lbl), 'r') as f:
            for line in f:
                parts = line.split()
                if len(parts) < 5: continue
                cls, x, y, w, h = map(float, parts)
                data.append({'class': int(cls), 'x': x, 'y': y, 'w': w, 'h': h, 'area': w * h})

    df = pd.DataFrame(data)
    names = {0: 'Sharp Tool (Hazard)', 1: 'Pothole (Hazard)', 2: 'Handheld Tool (Safe)'}
    df['name'] = df['class'].map(names)
    return df

