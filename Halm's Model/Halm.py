import time

def benchmark_model(model_path, name):
    model = YOLO(model_path)
    # Get model file size in MB
    file_size = os.path.getsize(model_path) / (1024 * 1024)

    # Measure inference time on a sample image (using CPU to simulate Raspberry Pi better)
    img = np.zeros((640, 640, 3), dtype=np.uint8)
    start_time = time.time()
    for _ in range(10):  # Run 10 times for average
        model.predict(img, device='cpu', verbose=False)
    avg_inference = (time.time() - start_time) / 10 * 1000 # in ms

    return {"Model": name, "Size (MB)": f"{file_size:.2f}", "Latency (ms)": f"{avg_inference:.2f}"}

# Run benchmark
nano_perf = benchmark_model("/content/drive/MyDrive/HALM_YOLO/halm_hazard_nano3/weights/best.pt", "Nano")
small_perf = benchmark_model("/content/drive/MyDrive/Halm_Small_Backup/weights/best.pt", "Small")

# Display as a clean table
perf_df = pd.DataFrame([nano_perf, small_perf])
