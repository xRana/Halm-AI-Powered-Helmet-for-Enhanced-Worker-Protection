def visualize_sample(img_dir, lbl_dir, num_samples=3):
    """Helper function to plot images with bounding boxes."""
    image_files = [f for f in os.listdir(img_dir) if f.lower().endswith(('.jpg', '.png', '.jpeg'))]
    if not image_files:
        print(f"No images found in {img_dir}")
        return

    samples = random.sample(image_files, min(num_samples, len(image_files)))
    fig, axes = plt.subplots(1, len(samples), figsize=(18, 6))
    if len(samples) == 1: axes = [axes]

    for i, img_name in enumerate(samples):
        img_path = os.path.join(img_dir, img_name)
        # Assuming labels are in a parallel 'labels' or 'annotated-labels' folder
        lbl_name = img_name.rsplit('.', 1)[0] + '.txt'
        lbl_path = os.path.join(lbl_dir, lbl_name)

        img = cv2.imread(img_path)
        img = cv2.cvtColor(img, cv2.COLOR_BGR2RGB)
        h, w, _ = img.shape

        if os.path.exists(lbl_path):
            with open(lbl_path, 'r') as f:
                for line in f:
                    parts = line.split()
                    if len(parts) < 5: continue
                    cls, x, y, bw, bh = map(float, parts[:5])
                    x1, y1 = int((x - bw/2) * w), int((y - bh/2) * h)
                    x2, y2 = int((x + bw/2) * w), int((y + bh/2) * h)
                    cv2.rectangle(img, (x1, y1), (x2, y2), (0, 255, 0), 3)

        axes[i].imshow(img)
        axes[i].set_title(f"Verify: {img_name}")
        axes[i].axis('off')
    plt.show()


