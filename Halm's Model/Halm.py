def process_and_merge_all(configs, split='train'):
    dest_img_dir = f'/content/halm_final/images/{split}'
    dest_lbl_dir = f'/content/halm_final/labels/{split}'
    os.makedirs(dest_img_dir, exist_ok=True)
    os.makedirs(dest_lbl_dir, exist_ok=True)

    for config in configs:
        name = config['name']
        src_img_dir = config['img_dir']
        src_lbl_dir = config['lbl_dir']
        new_id = config['new_id']
        is_sharp_tool = config.get('is_sharp_tool', False)

        print(f"\n🚀 Processing {name} (Class {new_id})...")
        count = 0
        rejected = 0

        img_files = [f for f in os.listdir(src_img_dir) if f.lower().endswith(('.jpg', '.jpeg', '.png'))]

        for img_name in img_files:
            stem = img_name.rsplit('.', 1)[0]
            img_path = os.path.join(src_img_dir, img_name)
            lbl_path = os.path.join(src_lbl_dir, stem + '.txt')

            if not os.path.exists(lbl_path): continue

            img = cv2.imread(img_path)
            if img is None: continue
            img_h, img_w, _ = img.shape

            with open(lbl_path, 'r') as f:
                lines = f.readlines()

            valid_yolo_lines = []
            for line in lines:
                line = line.replace(',', ' ').strip()
                parts = line.split()
                if not parts: continue

                try:
                    if is_sharp_tool:
                        # Pixel to YOLO logic for Class 0
                        x_min, y_min, px_w, px_h = map(float, parts[:4])
                        x_c = (x_min + (px_w / 2)) / img_w
                        y_c = (y_min + (px_h / 2)) / img_h
                        w_norm, h_norm = px_w / img_w, px_h / img_h
                    else:
                        # Standard YOLO logic for Class 1 & 2
                        vals = [float(x) for x in parts]
                        x_c, y_c, w_norm, h_norm = vals[1:5] if len(vals) == 5 else vals[0:4]

                    if 0 <= x_c <= 1 and 0 <= y_c <= 1:
                        valid_yolo_lines.append(f"{new_id} {x_c:.6f} {y_c:.6f} {w_norm:.6f} {h_norm:.6f}")
                    else:
                        rejected += 1
                except: rejected += 1

            if valid_yolo_lines:
                shutil.copy(img_path, os.path.join(dest_img_dir, img_name))
                with open(os.path.join(dest_lbl_dir, stem + '.txt'), 'w') as f_out:
                    f_out.write("\n".join(valid_yolo_lines))
                count += 1
        print(f"✅ {name}: Successfully merged {count} images.")

